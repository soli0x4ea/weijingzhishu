def get_tactile_description(scene_id, action=None):
    """获取触觉描述"""
    scene_data = TACTILE.get(scene_id, {})
    if action and ("feel_" + action in scene_data or action in scene_data):
        return scene_data.get("feel_" + action, scene_data.get(action, scene_data.get("default", "")))
    if action:
        # Try partial match
        for key in scene_data:
            if action.lower() in key.lower():
                return scene_data[key]
    return scene_data.get("default", "你的手指在黑暗中碰到了一些东西——但触感很模糊。")


